            4 -> 0xFF6B7280.toInt()  // 灰色 - 普通
            5 -> 0xFF16A34A.toInt()  // 绿色 - 有希望
            6 -> 0xFF0891B2.toInt()  // 青色 - 渐入佳境
            7 -> 0xFF2563EB.toInt()  // 蓝色 - 幸运
            8 -> 0xFF7C3AED.toInt()  // 紫色 - 欧气
            9 -> 0xFFD946EF.toInt()  // 粉紫 - 天命
            10 -> 0xFFFF6D00.toInt() // 橙色 - 超越
            else -> 0xFFFFD700.toInt()
        }

        return HoroscopeResult(
            zodiacName = zodiac.name,
            fortuneLevel = fortuneLevel,
            fortuneName = fortuneName,
            actionTip = actionTip,
            fortuneColor = fortuneColor,
            luckyColor = zodiac.luckyColor
        )
    }

    /**
     * 根据星座、运势等级和日期获取40条唯一行动建议
     * 算法：238条核心种子 × 确定性洗牌 = 每日40条唯一组合
     */
    private fun getTipsForZodiacAndLevel(
        zodiacName: String,
        fortuneLevel: Int,
        year: Int,
        month: Int,
        day: Int
    ): List<String> {
        val zodiacIndex = zodiacData.indexOfFirst { it.name == zodiacName }
        val dayOfYear = getDayOfYear(year, month, day)

        // 组合种子确保每日唯一：同一天同一星座同一等级永远返回相同40条
        val seed = zodiacIndex * 1000000 + fortuneLevel * 10000 + dayOfYear
        val rng = Random(seed)

        // 复制并洗牌
        val shuffled = coreActionTips.toMutableList()
        val n = shuffled.size
        // Fisher-Yates洗牌
        for (i in n - 1 downTo 1) {
            val j = rng.nextInt(i + 1)
            val tmp = shuffled[i]
            shuffled[i] = shuffled[j]
            shuffled[j] = tmp
        }

        // 取前40条，每条加上运势前缀和后缀
        val prefixList = fortuneLevelPrefixes[fortuneLevel - 1]
        val suffix = fortuneLevelSuffixes[fortuneLevel - 1]

        return (0 until 40).map { i ->
            val action = shuffled[i]
            val prefix = prefixList[(zodiacIndex + fortuneLevel + i) % prefixList.size]
            val processedAction = if (i % 3 == 0 && action.length > 30) {
                // 偶尔截取前半部分增加变化
                val parts = action.split("，")
                if (parts.size > 1) parts.take(2).joinToString("，") else action
            } else {
                action
            }
            prefix + processedAction + "。" + suffix
        }
    }

    /**
     * 计算一年中的第几天
     */
    private fun getDayOfYear(year: Int, month: Int, day: Int): Int {
        val daysInMonth = intArrayOf(0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31)
        var doy = day
        for (i in 1 until month) {
            doy += daysInMonth[i]
        }
        return doy
    }

